.global _start
_start:

    ldr r4, =Data
    ldr r5, =_Data
    ldr r6, =0x00001500
    ldr r7, =ASCII

Loop:
    cmp r4, r5
    beq terminate
	
    ldr r0, [r4], #4

    ldrb r1, [r7, r0]
    strb r1, [r6], #1

    b Loop

terminate:
    b terminate

.data
Data:
    .word 0xA,0xB,0xC,0x1,0x2,0x4,0x9,0x3
    .word 0xD,0xF,0xE,0x7,0x5,0x6,0x8,0x0
_Data:

ASCII:
    .byte 0x30,0x31,0x32,0x33,0x34,0x35,0x36,0x37
    .byte 0x38,0x39,0x41,0x42,0x43,0x44,0x45,0x46
_ASCII:

.end